import type { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: "*",
        allow: "/",
        disallow: ["/search"],
      },
    ],
    sitemap: "https://trademark.com.ua/sitemap.xml",
    host: "https://trademark.com.ua",
  };
}
